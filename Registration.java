   public static boolean isValidEmail(String email) {
       // create the EmailValidator instance
       EmailValidator validator = EmailValidator.getInstance();

       // check for valid email addresses using isValid method
       return validator.isValid(email);
   }
   
   public boolean verifyFields(){
       String uname = jTextFieldUsername.getText();
       String email = jTextFieldEmail.getText();
       String pass = String.valueOf(jPasswordField1.getPassword());
       String pass2 =String.valueOf(jPasswordField2.getPassword());
       
       if(uname.trim().equals("") || email.trim().equals("") || pass.trim().equals("") 
               || pass2.trim().equals("")){
           JOptionPane.showMessageDialog(null, "Please fill all fields","Empty fields",2);
           return false;
       
       }else if (!pass.equals(pass2)){
       
       JOptionPane.showMessageDialog(null, "Passwords doesn't match","Confirm password",2);
       return false;
       }else{
            return true;
       }
       
   }

   

    
    


