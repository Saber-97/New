    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        // TODO add your handling code here:
        
       

        String username = jTextFieldUsername.getText();
         String email = jTextFieldEmail.getText();
         String password = String.valueOf(jPasswordField1.getPassword());
        
        if(verifyFields()){
            String name = isValidEmail(email) ? "1":"0";
             if("1".equals(name)){

         
             
                    PreparedStatement ps;
                    
                    String query = "INSERT INTO `users`(`Username`, `Email`, `Password`, `balance`) VALUES (?,?,?,?) ";
                try {
                    ps = DbConnection.getConnection().prepareStatement(query);
                     ps.setString(1, username);
                    ps.setString(2, email);
                    ps.setString(3, password);
                    ps.setDouble(4, 500);
                    
                   
                        if(ps.executeUpdate() != 0){
              
                        MainMenu form = new MainMenu();
                        form.setVisible(true);
                        form.pack();
                        form.setLocationRelativeTo(null);
                        

                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Wrong credentials","Register error",2);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
                }
                   
                     
             
        }
             else{
                JOptionPane.showMessageDialog(null, "Wrong email details","Register error",2);
             }
        }
         
       
       
        
        

    }//GEN-LAST:event_jButtonLoginActionPerformed

